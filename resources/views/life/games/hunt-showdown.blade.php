@ru
  <p>Основная задача — вынести награду с босса, которого перед этим надо убить и изгнать. Убивать босса можно шумно или тихо. На шум могут успеть сбежаться враги, что вы не заметите этого. После убийства босса нужно запустить изгнание, которое длится 200 секунд. За это время враги со всей карты могут дойти до вас. Самое забавное, что враги по пути могут столкнуться друг с другом, что им уже будет не до вас. Если несколько команд оказалось на одной локации, то может начаться настоящий хаос, где все игроки перемешаются. Перестрелки запросто могут длиться несколько минут, что за это время будет много перемещений. Однако, с наградой с изгнанного босса можно просто сбежать, проигнорировав перестрелки.</p>
