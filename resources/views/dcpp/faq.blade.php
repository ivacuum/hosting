<p><b>Настройка активного режима</b></p>
<p>Для того, чтобы можно было работать в активном режиме без проблем, требуется настроить свое подключение к провайдеру таким образом, чтобы ничто не мешало программе DC++ принимать входящие соединения от других пользователей. Если это не сделано или сделано неправильно, то такой пользователь не сможет полноценно работать в сети даже несмотря на то, что он зарегистрирован в ней как активный.</p>
<ol class="mb-4">
  <li class="mb-4">
    <div class="font-bold">При работе напрямую</div>
    <p>Если Вы подключаетесь к провайдеру напрямую, то чаще всего - да практически всегда! - причиной блокировки приема входящих соединений является всего лишь неверно или не до конца настроенный системный firewall. Поэтому прежде всего необходимо убедиться, что для программы-клиента DC++ в нем разрешены не только исходящие, но и входящие соединения. Обычно для этого достаточно занести эту программу в список доверенных приложений в файрволле. Если же ваш файрволл не имеет такого списка, то потребуется добавить соответствующие правила ("разрешить для этого приложения все входящие и исходящие соединения по протоколам TCP и UDP") вручную.</p>
  </li>
  <li>
    <div class="font-bold">При работе через чужую сеть</div>
    <p>Другое дело, если Вы подключаетесь к провайдеру через домовую локальную сеть, и оттого вынуждены работать в пассиве. В этой ситуации тоже можно что-то предпринять, чтобы все же запустить активный режим. Но для этого будет уже недостаточно только Ваших усилий.</p>
    <p>Для того, чтобы вам можно было работать в активном режиме, администратор вашей локальной сети (или человек, отвечающий за работу ее шлюза в сеть провайдера, который часто называют в обиходе роутер или сервер) должен будет провести его дополнительную настройку. Более подробно о том, как именно настраивается работа активного режима через шлюз чужой сети, можно почитать в соответствующей статье. Если вы сами являетесь администратором локальной сети (например, вы подключаетесь через PPPoE и настроили ваше соединение в роутере), то можете прочитать соответсвующее руководство по настройке NAT (для роутера D-Link DSL-500T).</p>
    <p>Администратор, однако, может отказаться это проделать по различным причинам, от банального нежелания и лени до каких-либо технических препятствий. В таком случае вам лишь остается продолжать пользоваться пассивным режимом.</p>
  </li>
</ol>

<p><b>Проблемы и решения</b></p>
<p>Как же определить, что Вы пытаетесь работать в активном режиме за ненастроенным файрволлом или шлюзом? Если Вы работаете в активном режиме, и замечаете, что другие пользователи (особенно пассивы) ничего не могут с Вас скачать, жалуясь на ошибки Connection timeout - это один из признаков. Может быть и наоборот - с Вас качают нормально, а вот Вы не можете ни к кому подключиться с такой же ошибкой. Вы можете также вдруг обнаружить, что не работает поиск файлов.</p>
<p>Многие пользователи сами не подозревают, что у них есть и включен файрволл. Они могут решить, что раз никогда ничего подобного не устанавливали, то его у них нет. Это не всегда так. Например, в операционных системах Windows XP и Windows Server 2003 файрволл не просто есть, но и по умолчанию включен. Убедиться в этом можно, зайдя в список сетевых соединений и проверив наличие на их значках маленького желтого замка. Если он есть - системный файрволл включен, и ему тоже потребуется настройка.</p>
<p>Обычно системный файрволл, хотя его и достаточно для базовой защиты компьютера от внешних атак из сети, достаточно просто устроен и настраивается не очень гибко. Специализированные программы от третьих фирм, такие как Agnitum Outpost или Kerio Personal Firewall, обычно гораздо удобнее в настройке, надежнее и обладают множеством дополнительных возможностей. Однако, с другой стороны, они обычно и сложнее, а зачастую - и стоят денег.</p>
<p>Решать, ставить или не ставить альтернативный продукт в качестве файрволла, вам, но я бы посоветовал это все же сделать. Главное - в этом случае не забудьте отключить системный файрволл (снять галочки "Защитить это подключение..." в свойствах сетевых соединений). Когда на компьютере работают одновременно два файрволла - ни к чему хорошему это обычно не приводит. Глюки могут быть самыми фееричными.</p>

<p><b>Проблемы остались?</b></p>
<p>Даже после того, как Вы правильно провели всю настройку системы со своей стороны, и, наконец, вышли в сеть DC в активном режиме, вы можете столкнуться с ситуацией, когда перекачка не срабатывает. Бывает и такое, ведь нормальная работа двух пользователей активного режима между собой зависит от настроек их обоих. Вполне возможно, что проблема уже не у вас, а в том, что пользователь, с которого Вы пробуете качать (или который безуспешно пробует качать с Вас), не настроил у себя активный режим как полагается. В таком случае вы ничего не сможете поделать, кроме как сообщить ему о необходимости проверить его настройки.</p>
<p>Если же у Вас ничего так и не выходит - не отчаивайтесь. Прежде всего - внимательно перечитайте этот текст с самого начала. Убедитесь, что Вы поняли и выполнили предложенные в нем рекомендации. Если и это не разрешит ситуацию - обращайтесь за помощью непосредственно к администраторам хаба сети DC (это те ребята, у которых в списке вместо значка нарисованы ключики). </p>

