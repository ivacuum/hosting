<p>Это ТЕГИ пользователя. Они обозначают возможности клиента и режим работы.</p>
<p>Общий вид тэга: <b><++ V:x,M:x,H:x/y/z,S:x[,O:x]></b></p>
<ul>
  <li><b>V</b> — версия используемой программы</li>
  <li><b>M</b> — статус пользователя, <b>А</b> — актив, <b>Р</b> — пассив (за firewall'ом)</li>
  <li><b>H:x/y/z</b> — количество хабов, на которых находится пользователь</li>
