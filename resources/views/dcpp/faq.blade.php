  <li><b>...z</b> — количество хабов, на которых вы оператор хаба</li>
  <li><b>S</b> — количество слотов для скачивания</li>
  <li><b>O</b> — если общая отдача ниже этого значения, то DC++ откроет другой слот</li>
  <li><b>L</b> — лимит скорости, установленный юзером на скачивание с него</li>
</ul>
