<p><b>Общая информация</b></p>
<p>По большому счету, существует всего два основных режима соединения у DC. Один из них называется Активным (Active), а другой - соответственно, Пассивным (Passive). Есть и другие режимы, например, для работы через Socks5-прокси, но они достаточно экзотичны, и потому нам на сегодня неинтересны.</p>

<p><b>Почему они существуют?</b></p>
<p>Прежде всего, разделение режимов придумано для того, чтобы позволить работать в сети DC как можно большему количеству людей.</p>
<p>Люди подключаются к сети разными способами, которые часто налагают ограничения на возможности их работы. Сеть DC, как следует из ее названия (а DC расшифровывается как Direct Connect, "соединение напрямую"), строится на возможности установления прямых соединений между пользователями. Эти прямые соединения составляют самую суть работы в сети DC: с их помощью осуществляется как файловый обмен между пользователями, так и поиск информации в сети. Если между какими то двумя пользователями не удастся установить такого вот прямого соединения - они друг у друга ничего не смогут скачать и даже найти.</p>
<p>Если пользователь подключен к сети непосредственно (например, звонит провайдеру обычным модемом или имеет индивидуальное ADSL-подключение) - он обычно в состоянии как инициировать прямые соединения с другими пользователями сети (совершать исходящие соединения), так и принимать запросы на такие соединения от прочих пользователей (принимать входящие соединения). Такой пользователь может уведомить об этом хаб сети DC, подключившись к нему в активном режиме.</p>
<p>Однако, если пользователь работает с сетью DC через посредника (например, подключен через районную или домовую локальную сеть) - обычно он лишен возможности принимать входящие соединения, а может только совершать исходящие. Именно для такого случая и придуман пассивный режим работы пользователя, позволяющий ему хоть как-то работать с сетью DC даже с такого "урезанного в возможностях" подключения.</p>
<p>У внимательного читателя может возникнуть вопрос, как же так получается, что к пассивному пользователю вообще кто то подключается, если принимать соединения он не может? Вопрос хороший, а ответ на него еще лучше - никак не получается ;) В этом случае активный пользователь, желающий подключиться к пассивному, направляет ему через хаб, к которому подключены они оба, "просьбу" для пассива установить с ним соединение. Пассивный пользователь эту просьбу выполняет, и прямое соединение, таким образом, устанавливается по его инициативе как исходящее. </p>

<p><b>Чем они отличаются?</b></p>
<p>Пользователь активного режима - полноценный участник сети DC. Поскольку он "умеет" как устанавливать новые соединения с другими пользователями, так и принимать их от других, он ничем не ограничен в своих возможностях работы с сетью и поиска информации в ней.</p>
<p>Пользователь пассивного режима - не в состоянии принимать входящие соединения от других. Он может только устанавливать соединения по своей инициативе. Оттого он не в состоянии работать с другими пассивными пользователями сети DC (и в самом деле - если оба пользователя не умеют принимать соединения, то как они свяжутся друг с другом?). Кроме того, поскольку поиск тоже осуществляется через прямое соединение между пользователями, "пассив" вынужден пользоваться для поиска услугами хаба сети, что ограничивает количество возвращаемых результатов.</p>
<p>Говоря короче, нормальным режимом работы пользователя в сети DC является активный. Пассивный режим следует воспринимать как вынужденную меру, применяемую лишь в случаях непреодолимых проблем (как технического, так и административного характера) с активным подключением пользователя к сети DC.</p>

